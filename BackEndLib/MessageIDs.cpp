LANGUAGE Language::Get(const WCHAR* pwStr)
//Returns: language enum corresponding to full name
{
	string str;
	UnicodeToUTF8(pwStr, str);
	return Get(str.c_str());
}

LANGUAGE Language::Get(const char* pStr)
{
	if (strlen(pStr) != 3) //all codes are three characters
		return Unknown;

	for (UINT i=1; i<LanguageCount; ++i)
	{
		if (!stricmp(pStr, szLanguage[i]))
			return (LANGUAGE)i;
	}

	return Unknown;
}

const char* Language::GetCode(const LANGUAGE eLanguage)
{
	ASSERT(eLanguage < LanguageCount);
	return szLanguage[eLanguage];
}

